<div class="onp-help-section">
	<h1>Создание приложения Вконтакте</h1>

	<p>
		Приложение Вконтакте необходимо для использования следующих кнопок:
	</p>
	<ul>
		<li>Кнопка "мне нравится"</li>
		<li>Кнопка "подписаться"</li>
		<li>Кнопка "поделиться"</li>
	</ul>
</div>
<div class="onp-help-section">
	<p>1. Перейдите по <a href="https://vk.com/apps?act=manage" target="_blank">ссылке</a>.</p>
</div>
<div class="onp-help-section">
	<p>2. На странице управления вашими приложениями, нажмите кнопку "Создать приложение":</p>

	<p class="onp-img">
		<img src="http://cconp.s3.amazonaws.com/sociallocker/rus/vk_new/screen-1.jpg">
	</p>
</div>
<div class="onp-help-section">
	<p>3. Заполните форму, как на скриншоте ниже (используйте свои данные):</p>

	<p class="onp-img">
		<img src="http://cconp.s3.amazonaws.com/sociallocker/rus/vk_new/screen-2.jpg">
	</p>

	<p>После заполнения формы, ждем кнопку "Подключить сайт", вас попросят подтвердить создание приложения на вашем
		телефоне (смс или через установленное приложение Вконтакте)</p>
</div>
<div class="onp-help-section">
	<p>4. Подтверждаем создание приложения:</p>

	<p class="onp-img">
		<img src="http://cconp.s3.amazonaws.com/sociallocker/rus/vk_new/screen-3.jpg">
	</p>
</div>
<div class="onp-help-section">
	<p>5. После подтверждения создания приложения, мы попадаем на главную страницу нашего приложения. Нам нужно перейти
		на страницу настроек, жмем закладку "Настройки"</p>

	<p class="onp-img">
		<img src="https://cconp.s3.amazonaws.com/sociallocker/rus/vk/step-6.jpg">
	</p>
</div>
<div class="onp-help-section">
	<p>6. Создание приложения завершено, нам нужно проверить настройки и правильность заполненных полей. В поле "Адрес
		сайта" введите адрес вашего сайта http://site.ru. В поле базовый домен, введите домен на котором будет
		использоваться замок, если это поддомен, то указываем поддомен(new.site.ru) и основной домен(site.ru).</p>

	<p class="onp-img">
		<img src="http://cconp.s3.amazonaws.com/sociallocker/rus/vk_new/screen-4.jpg">
	</p>
</div>
<div class="onp-help-section">
	<p>7. Копируйте полученный ID приложения и секретный ключ.</p>

	<p class="onp-img">
		<img src="http://cconp.s3.amazonaws.com/sociallocker/rus/vk_new/screen-5.jpg">
	</p>
	Вставьте на странице <a
		href="<?php echo opanda_get_settings_url('social'); ?>">Общие настройки</a>, где вас попросят ввести ID и
	секретный ключ приложения Вконтакте.
	<p class="onp-img">
		<img src="http://cconp.s3.amazonaws.com/sociallocker/rus/vk_new/screen-6.jpg">
	</p>
</div>
